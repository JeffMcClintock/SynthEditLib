// Returns the SE 1.5 GDI-measured pixel dimensions for (family, size, bold, italic).
// Family is matched case-sensitively against the table. Sizes outside [4..20] are
// clamped. Unknown families fall back to Arial (the app's universal fallback).
inline Size lookup(const std::string& family, int size, bool bold, bool italic)
{
    if (size < 4) size = 4;
    if (size > 20) size = 20;

    const int b = bold ? 1 : 0;
    const int i = italic ? 1 : 0;

    // primary: exact family match.
    for (const auto& e : kTable)
    {
        if (e.size == size && e.bold == b && e.italic == i && family == e.family)
            return { e.pixelWidth, e.pixelHeight };
    }

    // fallback: Arial. Unusual fonts in SE skins also resolve to Arial at render time.
    for (const auto& e : kTable)
    {
        if (e.size == size && e.bold == b && e.italic == i && std::string("Arial") == e.family)
            return { e.pixelWidth, e.pixelHeight };
    }

    return { size / 2, size }; // unreachable given the Arial entries above.
}

